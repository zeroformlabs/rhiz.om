/////////////////////////////////////////
// BEING SCHEMA
/////////////////////////////////////////

export const BeingSchema = z.object({
  type: BeingTypeSchema,
  id: z.string().cuid(),
  name: z.string(),
  createdAt: z.coerce.date(),
  modifiedAt: z.coerce.date(),
  ownerId: z.string().nullable(),
  locationId: z.string().nullable(),
  state: z.string().nullable(),
  extIds: JsonValueSchema.nullable(),
  idHistory: JsonValueSchema.nullable(),
  metadata: JsonValueSchema.nullable(),
  properties: JsonValueSchema.nullable(),
  content: JsonValueSchema.nullable(),
})

export type Being = z.infer<typeof BeingSchema>

